// Program based on this tutorial:
// https://www.youtube.com/watch?v=aGJm3fuKLhA

.global _start

_start:

	// Shift left
	mov r0, #40   // 00000000000000000000000000101000 = 40
	lsl r0, r0, 1 // 00000000000000000000000001010000 = 80
	lsl r0, r0, 1 // 00000000000000000000000010100000 = 160

	// Shift right
	mov r0, #40   // 00000000000000000000000000101000 = 40
	lsr r0, r0, 1 // 00000000000000000000000000010100 = 20
	lsr r0, r0, 1 // 00000000000000000000000000001010 = 10

	// Arithmetic shift right
	mov r0, #-40  // 11111111111111111111111111011000 = 4294967256
	asr r0, r0, 1 // 11111111111111111111111111101100 = 4294967276
	asr r0, r0, 1 // 11111111111111111111111111110110 = 4294967286

	// Rotate right
	mov r0, #-40  // 11111111111111111111111111011000 = 4294967256
	ror r0, r0, 1 // 01111111111111111111111111101100 = 2147483628
	ror r0, r0, 1 // 00111111111111111111111111110110 = 1073741814
	ror r0, r0, 1 // 00011111111111111111111111111011 = 536870907
	ror r0, r0, 1 // 10001111111111111111111111111101 = 2415919101
